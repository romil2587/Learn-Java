package Basics;

public class Arithmetic_Operators {

	public static void main(String[] args) {
	
		int a = 20;
		int b = 10;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b); // for modulus
		
		a++;
		b--;
		System.out.println(a);
		System.out.println(b);
		
		boolean bool= true;
		System.out.println(!bool);
				
	}

}
