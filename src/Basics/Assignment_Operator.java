package Basics;

public class Assignment_Operator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println(a+=b); // a=30
		System.out.println(a-=b); // a=10
		System.out.println(a*=b); // a=200
		System.out.println(a/=b); // a=10
		System.out.println(a%=b); // a= 10
	}

}
